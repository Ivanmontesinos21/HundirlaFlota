package HundirlaFlota;

public class Ganador {
    public static void main(String[] args) {
        int[] barcos = { 4, 3, 3, 2};



    }
    public static int sumaCeldas(int[] barcos){
        int sumceldas=0;
        for (int i=0;i<barcos.length;i++ )
            sumceldas+=barcos[i];
        return sumceldas;

    }

}

